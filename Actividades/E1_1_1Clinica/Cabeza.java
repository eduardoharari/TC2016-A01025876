public class Cabeza
{
    private String barba;
    private String cabello;

    public Cabeza()
    {

    }
    public Cabeza(String barba, String cabello)
    {
        this.barba = barba;
        this.cabello = cabello;
    }
    public String getBarba()
    {
        return barba;
    }
    public String getCabello()
    {
        return cabello;
    }
    


 
}