/**
 * Ceja
 */
public class Ceja 
{
    private String color;
    private double grosorCm;

    public Ceja()
    {

    }
    public Ceja(String color, double grosorCm)
    {
        this.color = color;
        this.grosorCm = grosorCm;

    }
    public String getColor()
    {
        return color;
    }
    public double getGrosorCm()
    {
        return grosorCm;
    }

    
}