/**
 * Labio
 */
public class Labio 
{
    private int numberOfLabio;
    private String color;
    
    private Labio()
    {

    }
    public Labio(int numberOfLabio, String color)
    {
        this.numberOfLabio = numberOfLabio;
    }
    public int getNumberOfLabio()
    {
        return numberOfLabio;
    }
    public String getColor()
    {
        return color;
    }

    
}