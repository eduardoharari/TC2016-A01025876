
public class Flotantes 
{
    float value = 0.0f;

    public Flotantes()
    {

    }
    public Flotantes(float value)
    {
        this.value = value;
    }
    public float getValue()
    {
        return value;
    }
    public String toString()
    {
        return value + " ";
    }
    public void printValue()
    {
        System.out.println(value);
    }

    
}
